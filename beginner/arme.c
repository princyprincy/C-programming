#include<stdio.h>
void main()
{
	int array[10],i,j,t,n,m;
	scanf("%d",&n);
	for(i=0;i<n;i++)
	{
		scanf("%d",&array[i]);
	}
	for(i=0;i<n;i++) 
	{
		for(j=i+1;j<n;j++)
		{
			if(array[i]>array[j]) 
			{
				t=array[i];
				array[i]=array[j];
				array[j]=t;
			}
		}
	}
	if(n%2==0)
	{
		m=(array[n-1/2]+(array[n/2]))/2;
	}
	else
	{
		m=array[n/2];
	}
	printf("\n%d",m);
}
